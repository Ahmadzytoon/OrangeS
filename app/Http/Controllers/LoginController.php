     */
    public function store(Request $request)
    {
        $request->validate([
            'email' => 'required',
            'password' => 'required',
        ]);
        $user = User::where('email', $request->email)->first();

        if(!empty($user)){
            if(Hash::check($request->password, $user->password)){
                session()->put('email', $user->email);
                session()->put('id', $user->id);
                if($user->email=='admin@gmail.com'){
                  return redirect()->route('admin.Results_Table.index');
                }
                else{         
                         return redirect()->route('user.voting.index');
                }
            } else {
                return redirect()->back()->withErrors(['password' => 'Invalid login']);
            }
        } else {
            return redirect()->back()->withErrors(['email' => 'Invalid login']);
